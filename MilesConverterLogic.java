class MilesConverterLogic {
    private float miles;
    private float km;

    public MilesConverterLogic() {
        
    }

    public void setMiles(float m) {
        this.miles = m;
    }

    public float getMiles() {
        return this.miles;
    }

    public void setKm(float k) {
        this.km = k;
    }

    public float setKm() {
        return this.km;
    }
}